public class EvenMoreLoops {
  public static void main(String[] args) {
    int a = 1;
    int i = 0, j = 0, k = 0, l = 0, m = 0, n = 0, o = 0, p = 0, q = 0, r = 0, s = 0, t = 0, u = 0,
        v = 0, w = 0;
    for (; i < 4; i++) {
      a += i;
    }
    for (; j < 4; j++) {
      a += i + j;
    }
    for (; k < 4; k++) {
      a += i + j + k;
    }
    for (; l < 4; l++) {
      a += i + j + k + l;
    }
    for (; m < 4; m++) {
      a += i + j + k + l + m;
    }
    for (; n < 4; n++) {
      a += i + j + k + l + m + n;
    }
    for (; o < 4; o++) {
      a += i + j + k + l + m + n + o;
    }
    for (; p < 4; p++) {
      a += i + j + k + l + m + n + o + p;
    }
    for (; q < 4; q++) {
      a += i + j + k + l + m + n + o + p + q;
    }
    for (; r < 4; r++) {
      a += i + j + k + l + m + n + o + p + q + r;
    }
    for (; s < 4; s++) {
      a += i + j + k + l + m + n + o + p + q + r + s;
    }
    for (; t < 4; t++) {
      a += i + j + k + l + m + n + o + p + q + r + s + t;
    }
    for (; u < 4; u++) {
      a += i + j + k + l + m + n + o + p + q + r + s + t + u;
    }
    for (; v < 4; v++) {
      a += i + j + k + l + m + n + o + p + q + r + s + t + u + v;
    }
    for (; w < 4; w++) {
      a += i + j + k + l + m + n + o + p + q + r + s + t + u + v + w;
    }
    System.out.println(a);
  }
}